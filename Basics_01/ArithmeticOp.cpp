#include<iostream>
using namespace std;
int main(){
    int num1, num2;
    cout<<"Enter the value for number1, number2\n";
    cin>>num1>>num2;
    cout<<"Addition of two numbers: "<< num1 + num2 <<endl;
    cout<<"Subtraction of two number: "<< num1 - num2 <<endl;
    cout<<"Multiplication of two number: "<< num1 * num2 <<endl;
    cout<<"Division of two number: "<< num1 / num2 <<endl;
    cout<<"Moduluse of two number: "<< num1 % num2<<endl;
    cout<<"Increment of number1: "<< ++num1 <<endl;
    cout<<"Decrement of number1: "<< --num1 <<endl;
    
    return 0;
}
